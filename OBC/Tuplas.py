gamestuple = ("Fifa24", "Red Dead 2", "Star wars", "Batman Arkan knight", 
              "Call of duty")
print(gamestuple)
print(type(gamestuple))

# 1 - Buscar os dois primeiros itens da tupla
print(gamestuple[:2])

# 2 - Buscar o último item da lista
print(gamestuple[-1])

#3 - Buscar hogos até uma determinada posição
print(gamestuple[:3])

# 4 - buscar jogos de uma posição em diante

print(gamestuple[2:])

# 5 - Recuperar um item da tupla pelo indice
print(gamestuple.index("Red Dead 2"))


