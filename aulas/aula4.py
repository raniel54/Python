#Tipos Int e Float
#int -> numero inteiro
#O tipo int representa qualquer numero
#positibo ou negativo. int sem sinal é considerado positivo

print(11) #int
print(-11) #int
print(0) #int


#float -> Número com ponto flutuante
# O tipo float representa qualquer número
# positivo ou negativo com ponto flutuante.
# float sem sinal é considerado positivo.
print(1.1, 10.11)
print(0.0, -1.5)

# A função type mostra o tipo que o python inferiu ao valor.

print(type('Oliveira')) #str
print(type(0)) #int
print(type(1.1)) #float