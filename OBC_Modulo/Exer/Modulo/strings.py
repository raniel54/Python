## Módulo de strings

#1. Inverter uma string de trás pra frente.
def invertida(string):
    return string[::-1]

#2 Retornar apenas letras com índice par.
def even_characters(string):
    return string[::2]

#3 Retornar apenas letras com índice ímpar.

def odd_characters(string):
    return string[1::2]